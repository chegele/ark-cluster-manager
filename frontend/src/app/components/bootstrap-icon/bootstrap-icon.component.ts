  ngOnInit() {
    if (this.preset?.toLowerCase()) {
      this.implementPreset(BootstrapIconComponent.ICONS[this.preset]);
    } else {
      if (!this.icon) throw new Error("You must provide either preset or icon.");
      if (!this.paths) throw new Error("You must provide either preset or paths");
    }
  }

  ngOnChanges(changes: SimpleChanges) {
    const update = changes?.['preset']?.currentValue;
    if(!update) return;
    const preset = BootstrapIconComponent.ICONS[update];
    if (!preset) return;
    this.implementPreset(preset);
  }

  private implementPreset(preset: BootstrapIcon) {
    this.icon = preset.cssClass;
    this.paths = preset.paths;
  }

